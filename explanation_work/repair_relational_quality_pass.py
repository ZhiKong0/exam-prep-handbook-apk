def load_json(path: Path):
    return json.loads(path.read_text(encoding="utf-8"))


def save_json(path: Path, data) -> None:
    path.write_text(json.dumps(data, ensure_ascii=False, indent=2), encoding="utf-8")


def reason_from_quick(quick: str) -> str:
    head = quick.split("\n\n选项判断", 1)[0].strip()
    head = re.sub(r"^本题理由：\s*", "", head)
    return head


def shorten(text: str, limit: int = 140) -> str:
    text = " ".join(text.split())
    if len(text) <= limit:
        return text
    for sep in ("。", "；", "，"):
        index = text.find(sep)
        if 20 <= index <= limit:
            return text[: index + 1]
    return text[:limit].rstrip() + "..."


def repair_chapter1() -> None:
    rows = load_json(CHAPTER1)
    for row in rows:
        reason = shorten(reason_from_quick(row.get("quickExplanation", "")))
        row["knowledgeDetail"] = re.sub(
            r"(?m)^  - 作答结论：答案为 .+$",
            f"  - 本题落点：{reason}",
            row.get("knowledgeDetail", ""),
        )
    save_json(CHAPTER1, rows)


