function validateEssays() {
  const seen = new Set();
  for (const item of essays) {
    if (seen.has(item.bookId)) throw new Error(`Duplicate essay bookId: ${item.bookId}`);
    seen.add(item.bookId);
    for (const key of ["chapter", "knowledge", "stem", "answer", "quick", "detail"]) {
      if (!item[key] || !String(item[key]).trim()) throw new Error(`Missing ${key} for ${item.bookId}`);
    }
  }
  const ids = essays.map((x) => x.bookId);
  if (JSON.stringify(ids) !== JSON.stringify(expectedOrder)) {
    throw new Error(`Essay order/list mismatch.\nactual=${ids.join(",")}\nexpected=${expectedOrder.join(",")}`);
  }
}

function toQuestion(item, id) {
  return {
    id,
    label: `大题-${item.bookId}`,
    type: "essay",
    typeName: "大题",
    stem: item.stem,
    options: [],
    answer: item.answer,
    images: [],
    knowledge: item.knowledge,
    chapter: item.chapter,
    blankCount: 0,
    quickExplanation: item.quick,
    knowledgeDetail: item.detail,
    explanation: `【快速做题】\n${item.quick}\n\n【知识点详解】\n${item.detail}`,
  };
}

function typeDistribution(questions, chapter) {
  const counts = new Map();
  for (const q of questions) {
    if (q.chapter !== chapter) continue;
    counts.set(q.typeName, (counts.get(q.typeName) || 0) + 1);
  }
  return Array.from(counts.entries()).map(([name, count]) => `${name}${count}题`).join("，");
}

function appendUnique(array, values) {
  const set = new Set(array);
  for (const value of values) {
    if (!set.has(value)) {
      array.push(value);
      set.add(value);
    }
  }
}

function updateOverviewCards(overrides, questions) {
  const essayLabelsByChapter = new Map();
  for (const item of essays) {
    const label = `大题-${item.bookId}`;
    const list = essayLabelsByChapter.get(item.chapter) || [];
    list.push(label);
    essayLabelsByChapter.set(item.chapter, list);
  }

  for (const card of overrides) {
    if (card.mode !== "insert" || !essayLabelsByChapter.has(card.chapter)) continue;
    card.labels = Array.isArray(card.labels) ? card.labels.filter((x) => !String(x).startsWith("大题-")) : [];
    appendUnique(card.labels, essayLabelsByChapter.get(card.chapter));
    card.questionCount = card.labels.length;
    card.typeDistribution = typeDistribution(questions, card.chapter);
    card.questionTips = Array.isArray(card.questionTips) ? card.questionTips : [];
    appendUnique(card.questionTips, [
      `本章已补充标记大题：${essayLabelsByChapter.get(card.chapter).join("、")}。复习时先按题号定位，再看“标准答案 + 小白解释 + 公式/过程”。`,
    ]);
    card.mustRemember = Array.isArray(card.mustRemember) ? card.mustRemember : [];
    appendUnique(card.mustRemember, [
      "新增大题采用自评：刷题中查看答案后点“会了/不会”，不会的题进入错题本；错题本中自评会了达到设置次数后移除。",
    ]);
  }
}

function writeJson(file, value) {
  fs.writeFileSync(file, JSON.stringify(value, null, 2) + "\n", "utf8");
}

function main() {
  validateEssays();
  const before = JSON.parse(fs.readFileSync(questionsPath, "utf8"));
  const stripped = before.filter((q) => !(q.label && String(q.label).startsWith("大题-")));
  let nextId = Math.max(...stripped.map((q) => q.id)) + 1;
  const newQuestions = essays.map((item) => toQuestion(item, nextId++));
  const combined = stripped.concat(newQuestions);
  combined.forEach((q, index) => {
    q.id = index + 1;
  });
  writeJson(questionsPath, combined);

  const overrides = JSON.parse(fs.readFileSync(overridesPath, "utf8"));
  updateOverviewCards(overrides, combined);
  writeJson(overridesPath, overrides);

  const chapterCounts = {};
  for (const q of combined) {
    chapterCounts[q.chapter] = (chapterCounts[q.chapter] || 0) + 1;
  }
  const essayCounts = {};
  for (const q of newQuestions) {
    essayCounts[q.chapter] = (essayCounts[q.chapter] || 0) + 1;
  }
  const report = [
    "# 标记大题导入报告",
    "",
    `- 原题数：${stripped.length}`,
    `- 新增大题：${newQuestions.length}`,
    `- 当前总题数：${combined.length}`,
    `- 新增题号：${newQuestions.map((q) => q.label).join("、")}`,
    "",
    "## 新增大题分布",
    "",
    ...Object.entries(essayCounts).map(([chapter, count]) => `- ${chapter}：${count} 题`),
    "",
    "## 当前章节总题数",
    "",
    ...Object.entries(chapterCounts).map(([chapter, count]) => `- ${chapter}：${count} 题`),
    "",
  ].join("\n");
  fs.mkdirSync(path.dirname(reportPath), { recursive: true });
  fs.writeFileSync(reportPath, report, "utf8");
  console.log(`Imported ${newQuestions.length} essay questions. Total=${combined.length}`);
}

main();
