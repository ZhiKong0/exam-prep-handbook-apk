function quick(answer, reason, extra = "") {
  return [
    `本题答案：${answer}`,
    `本题理由：${reason}`,
    extra,
  ].filter(Boolean).join("\n");
}

function detail({ core, table, steps, traps }) {
  const parts = [];
  parts.push("核心知识点：\n" + core.map((x) => `- ${x}`).join("\n"));
  if (table && table.length) {
    parts.push([
      "知识关系表：",
      "| 关系节点 | 本题怎么用 | 易错边界 |",
      "|---|---|---|",
      ...table.map((row) => `| ${row[0]} | ${row[1]} | ${row[2]} |`),
    ].join("\n"));
  }
  if (steps && steps.length) {
    parts.push("公式/过程：\n" + steps.map((x) => `- ${x}`).join("\n"));
  }
  if (traps && traps.length) {
    parts.push("易错点：\n" + traps.map((x) => `- ${x}`).join("\n"));
  }
  return parts.join("\n\n");
}

